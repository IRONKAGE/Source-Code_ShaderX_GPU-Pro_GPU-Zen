#include "Vectors.h"

enum ClipResult {
    CLIP_OUT,
    CLIP_IN,
    CLIP_INTERSECT
};

struct klPlane {
    klVec3 normal;
    float dist;

    void normalize(void) {}
    klPlane operator-(void) { return klPlane(); }
    ClipResult planeSide(klVec3 mins, klVec3 max) { return CLIP_IN; }
};