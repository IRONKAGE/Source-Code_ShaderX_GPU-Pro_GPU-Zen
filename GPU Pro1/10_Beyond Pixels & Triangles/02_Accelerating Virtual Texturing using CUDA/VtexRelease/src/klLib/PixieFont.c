int DrawChar(unsigned char *rgbaBuff, int pitch, int theChar, unsigned int color) {
    int cAdj, columns, i, y;

    if ( theChar < firstchr_S || theChar > (nr_chrs_S+firstchr_S) ) {
        return 0;
    }

    // Draw the character
    pitch /= 4;//in ints
    cAdj = theChar-firstchr_S;
    columns = lentbl_S[cAdj];
    for ( i=0; i<columns; i++ ) {
        int bits = chrtbl_S[cAdj][i];
        int *dst = ((int *)rgbaBuff)+i;
        for ( y=0; y<8; y++, dst+=pitch ) {
            int pix = bits&(1<<y);
            if ( pix ) {
                *dst = color;
            }
        }
    }            
    return columns;
}